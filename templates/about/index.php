<h3>Why?!</h3>

<p>If anyone has complained to you that you have not kept in touch with them, this software is for you.</p>

<p>Friendlee was created to <strong>solve the problem of keeping in touch</strong> with people. After entering your interaction details for a couple of weeks, the system will have enough data to warn you if you haven't contacted a friend for a while. You can put people in different categories - people who you want to contact once a week, once a month or once in three months. The system will give you a reminder if you haven't contacted the friend for that time period.</p>

<p>Also, there are other advantages to using friendlee. 

<ul>
<li>you can see <strong>trends</strong> - see who you are meeting more often, what kind of interaction you prefer, etc.</li>
<li>you have a <strong>history</strong> of the interaction you had with everyone around you.</li>
<li>you have a friends <strong>leader board</strong>. </li>
<li>the <strong>point</strong> system</li>
</ul>
</p>

<h4>Point System?</h4>

<p>Friendlee attaches points to each kind of interaction you have. In this system, a <strong>meeting is worth 10 points, phone call is 5 points, a whatsapp/sms conversation is 3 points and a chat conversation is worth 2</strong>.</p>

<p>Why points? Well, because its fun. Because then you manage to quantify a relationship. Because I'm weird that way. But most of all, because you can attach rewards to it. Me, I send you a photo of yourself every time you cross the 25 points mark. You can do other things - like buy them a chocolate. Its a fun way to incentivize others to keep in touch with you.</p>

